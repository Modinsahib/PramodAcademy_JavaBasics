package july132024;

public class ArithmeticOperator {
    public static void main(String[] args) {
        // Arithmetic Operator +,-,/,*,
        // %
        int a = 10;
        int b = 23;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // %
        System.out.println(a%b); // Remainders
    }
}
