package july132024;

public class UnaryOperator {
    public static void main(String[] args) {
        // Unary Operator
        int age = +65;
        int lambo = -1;
        System.out.println(age);
        System.out.println(lambo);

        int age1 = 65; // unary plus
        int num2 = -1; // unary minus -
        System.out.println(age1+num2); //+ - = -
    }

}
